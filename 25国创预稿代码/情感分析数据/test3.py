import os
import pandas as pd
from pathlib import Path


def count_data_files_info(target_dir: str = "."):
    """
    统计指定目录下所有数据文件的列数和数据行数（评论条数）

    Args:
        target_dir: 目标目录路径，默认为当前目录
    """
    # 支持的文件格式
    supported_formats = ['.csv', '.xlsx', '.xls']
    total_files = 0
    total_columns = 0  # 按文件维度的列数总和（非去重）
    total_comments = 0

    print("=" * 60)
    print(f"开始统计目录: {os.path.abspath(target_dir)}")
    print("=" * 60)

    # 遍历目录下的所有文件
    for file_path in Path(target_dir).glob("*"):
        if file_path.is_file() and file_path.suffix.lower() in supported_formats:
            total_files += 1
            try:
                # 根据文件格式读取数据
                if file_path.suffix.lower() == '.csv':
                    df = pd.read_csv(file_path, encoding='utf-8')
                else:  # Excel文件
                    df = pd.read_excel(file_path)

                # 获取文件信息
                file_name = file_path.name
                column_count = len(df.columns)
                comment_count = len(df)  # 数据行数（评论条数）

                # 累加总计
                total_columns += column_count
                total_comments += comment_count

                # 打印单个文件信息
                print(f"文件: {file_name}")
                print(f"  - 列数: {column_count}")
                print(f"  - 评论条数: {comment_count}")
                print("-" * 40)

            except Exception as e:
                print(f"⚠️ 处理文件 {file_path.name} 时出错: {str(e)}")
                print("-" * 40)

    # 打印汇总信息
    print("=" * 60)
    print("统计汇总:")
    print(f"- 有效文件总数: {total_files}")
    print(f"- 所有文件列数总和: {total_columns}")
    print(f"- 评论数据总行数: {total_comments}")
    print("=" * 60)


